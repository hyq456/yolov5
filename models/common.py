class SFB(nn.Module):
    # Concatenate a list of tensors along dimension
    def __init__(self, c):
        super(SFB, self).__init__()
        self.conv = Conv(c, c, 3, 1, 1)

    def forward(self, x):
        if len(x) == 2:
            x = self.conv(torch.add(x[0],x[1]))
        elif len(x) == 3:
            x = self.conv(torch.add(torch.add(x[0],x[1]),x[2]))
        return x

